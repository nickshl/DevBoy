// *****************************************************************************
// ***   Process   *************************************************************
// *****************************************************************************
bool GarioSprite::Process(int32_t dx, int32_t dy, int32_t tick_ms, int32_t time_ms)
{
  // Alive by default
  bool alive = true;

  // ***   Visual effects   ****************************************************
  if(dx < 0)
  {
    // Gario moves left - set flip
    SetHorizontalFlip(true);
    if(time_ms%X_SPRITE_ANIM_SPEED == 0) SetImage(gario[move_idx++]);
  }
  else if(dx > 0)
  {
    // Gario moves right - reset flip
    SetHorizontalFlip(false);
    if(time_ms%X_SPRITE_ANIM_SPEED == 0) SetImage(gario[move_idx++]);
  }
  else
  {
    // Gario stops - set stop image
    SetImage(gario[1]);
    move_idx = 0;
  }
  // Check animation variable
  if(move_idx >= 4) move_idx = 0;
  // Sprites for jump/fall
  if(y_speed < 0)
  {
    // Jump with hand
    SetImage(gario[5]);
  }
  else if(y_speed/1000000 != 0)
  {
    // Fall with hand
    SetImage(gario[0]);
  }

  if(is_die)
  {
    // Gario die - set stop image
    SetImage(gario[6]);
  }

  // ***   Y movement   ********************************************************
  // Set speed
  if( (dy < 0) && (y_speed == 0) ) y_speed = -JUMP_SPEED;
  if( (dy >= 0) && (y_speed < 0) && (jump == false) ) y_speed = Y_ACCEL_CONST; // For move to 1 pixel
  // Speed can't be zero
  if(y_speed == 0) y_speed = Y_ACCEL_CONST; // For move to 1 pixel
    // Increase speed
  y_speed += Y_ACCEL_CONST;
  // If speed equal zero, we need check fall to holes
  if(y_speed == 0)
  {
    y_speed = Y_ACCEL_CONST; // For start falling or clear speed
  }

  // Find new Y position in pixels
  int32_t y_end_pos = y_map_pos + (y_speed_rest + y_speed)/1000000;
  // Calculate speed rest for next iteration 
  y_speed_rest = (y_speed_rest + y_speed)%1000000;
  // Y axis movement - cycle for accuracy
  while((y_map_pos != y_end_pos) && alive)
  {
    // If possible move to the up
    if(   (y_speed > 0)
       && (tile_map.GetLvlIdxByXY(x_map_pos, y_map_pos + GetHeight() + 1) == -1)
       && (tile_map.GetLvlIdxByXY(x_map_pos + GetWidth(), y_map_pos + GetHeight() + 1) == -1) )
    {
      alive = false;
    }
    else if ((y_speed > 0) && (is_die))
    {
      y_map_pos++;
    }
    else if(   (y_speed > 0)
            && (tile_map.GetLvlDataByXY(x_map_pos, y_map_pos + GetHeight() + 1) >= 10)
            && (tile_map.GetLvlDataByXY(x_map_pos + GetWidth(), y_map_pos + GetHeight() + 1) >= 10))
    {
      y_map_pos++;
    }
    // If possible move to the down
    else if(y_speed < 0)
    {
      if(   (y_map_pos > 0)
         && (tile_map.GetLvlDataByXY(x_map_pos, y_map_pos - 1) >= 10)
         && (tile_map.GetLvlDataByXY(x_map_pos + GetWidth(), y_map_pos - 1) >= 10) )
      {
        y_map_pos--;
      }
      else
      {
        // When we reach any block - reset speed
        y_speed = 10;
        // Clear speed rest
        y_speed_rest = 0;
        // Clear jump flag
        jump = false;
        // Exit from cycle
        break;
      }
    }
    else
    {
      // When we reach down - reset speed
      y_speed = 0;
      // Clear speed rest
      y_speed_rest = 0;
      // Exit from cycle
      break;
    }
  }

  // ***   X movement   ********************************************************
  // Set speed
  if(dx != 0) x_speed = dx;
  else        x_speed = 0;
  // Calculate x movement
  int32_t x_end_pos = x_map_pos + (x_speed_rest + x_speed)/1000;
  // Calculate rest for next cycle
  x_speed_rest = (x_speed_rest + x_speed)%1000;
  // We can't move behind map
  if(x_end_pos < 0) x_end_pos = 0;
  // X axis movement - cycle for accuracy
  while((x_map_pos != x_end_pos) && (is_die == false))
  {
    // If possible move to the left
    if(   (dx < 0)
       && (tile_map.GetLvlDataByXY(x_map_pos-1, y_map_pos) >= 10)
       && (tile_map.GetLvlDataByXY(x_map_pos-1, y_map_pos + GetHeight()) >= 10) )
    {
      // Decrease position
      x_map_pos--;
      // Call check enemys here
    }
    // If possible move to the right
    else if(   (dx > 0)
            && (tile_map.GetLvlDataByXY(x_map_pos + GetWidth() + 1, y_map_pos) >= 10)
            && (tile_map.GetLvlDataByXY(x_map_pos + GetWidth() + 1, y_map_pos + GetHeight()) >= 10) )
    {
      // Increase position
      x_map_pos++;
      // Call check enemies here
    }
    else
    {
      // Otherwise exit from cycle
      break;
    }
  }
  // If Gario can't move - clear X speed
  if(x_map_pos != x_end_pos)
  {
    x_speed = 0;
    x_speed_rest = 0;
  }

  // ***   Tilemap scroll   ****************************************************
  // Gario can't walk back
  if(x_map_pos < tile_map.GetMapPosX()) x_map_pos = tile_map.GetMapPosX();
  // Scroll map    
  if(x_map_pos - tile_map.GetMapPosX() > tile_map.GetWidth()/2)
  {
    tile_map.ScrollView(x_map_pos - tile_map.GetMapPosX() - tile_map.GetWidth()/2);
  }
  // Prevent scroll map over screen
  if(tile_map.GetMapPosX() >= tile_map.GetPixWidth() - tile_map.GetWidth())
  {
    tile_map.ScrollView((tile_map.GetPixWidth() - tile_map.GetWidth()) - tile_map.GetMapPosX());
  }

  // ***   Gario sprite movement   *********************************************
  Move(x_map_pos - tile_map.GetMapPosX(), y_map_pos - tile_map.GetMapPosY());

  return alive;
}

